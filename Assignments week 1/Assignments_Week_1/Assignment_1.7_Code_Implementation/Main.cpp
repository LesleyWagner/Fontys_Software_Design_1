// include statements
#include <iostream>;

using namespace std;

int main() {
	// variable declarations
	int num1;
	int num2;
	int num3;
	int average;

	// executable statements
	num1 = 125;
	num2 = 28;
	num3 = -25;
	average = (num1 + num2 + num3) / 3;

	// output statement
	cout << "num1: " << num1 << ", num2: " << num2 << ", num3: " << num3 << " and average: " << average << endl;
}